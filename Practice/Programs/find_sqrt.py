'''find the square root
way 1 : exponential operartor

'''
num = 4;
print(f"Square root of {num} is : ", num**(0.5));


'''find the square root
way 2 : using built-in function

'''

import math;
print(f"Square root of {num} is : ", math.sqrt(num));

'''
1. Exponential Operator: Use the ** operator to raise a number to the power of 0.5. For example, num**(0.5) will give you the square root of num.

2. Using Built-in Function: Import the math module and use the sqrt() function to find the square root of a number. For example, math.sqrt(num) will give you the square root of num.

'''